    private void bAddJugadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bAddJugadorMouseClicked
        // TODO add your handling code here:

        String Usuario = tUsuario.getText();
        String Clave = tClave.getText();

        boolean encontrado = false;

        for (int i = 0; i < aJugadores.sizeJugadores(); i++) {
            String jugador = aJugadores.getJugador(i).getNombre();
            if (jugador.equals(Usuario)) {
                encontrado = true;
            }
        }

        if (Usuario.equals("") || Clave.equals("")) {
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "Debe rellenar los dos campos");

        } else if (encontrado == true) {
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "El nombre de Usuario ya est叩 en uso");

        } else {
            Jugador jugador = new Jugador(Usuario, Clave);
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "Jugador a単adido con 辿xito");

            aJugadores.addJugador(jugador);
            cListaJugadores.addItem(Usuario);
            resetJugadores();
        }

    }//GEN-LAST:event_bAddJugadorMouseClicked

    private void bEditUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bEditUsuarioMouseClicked

        // TODO add your handling code here:
        String usuario = tUsuario.getText();
        String clave = tClave.getText();

        String nuevo = tUsuarioChange.getText();

        boolean encontrado = false;
        boolean nuevoRepe = false;
        int j = 0;

        for (int i = 0; i < aJugadores.sizeJugadores(); i++) {
            String jugador = aJugadores.getJugador(i).getNombre();
            if (jugador.equals(usuario)) {
                encontrado = true;
                j = i;
            }

        }

        for (int i = 0; i < aJugadores.sizeJugadores(); i++) {
            String jugador = aJugadores.getJugador(i).getNombre();
            if (jugador.equals(nuevo)) {
                nuevoRepe = true;
            }
        }

        if (usuario.equals("") || encontrado == false) {
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "Debe ser un usuario existente");
        } else if (nuevoRepe == true) {
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "El nombre nuevo ya existe");

        } else {

            aJugadores.getJugador(j).setNombre(nuevo);

            resetJugadores();
            JFrame jFrame = new JFrame();
            JOptionPane.showMessageDialog(jFrame, "Usuario editado con 辿xito.");
        }
    }//GEN-LAST:event_bEditUsuarioMouseClicked

